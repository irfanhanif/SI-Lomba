public class DosenObjek {
    String nama;
    String noHp;

    public DosenObjek(String nama, String noHp) {
        this.nama = nama;
        this.noHp = noHp;
    }

    public String getNama() {
        return nama;
    }

    public String getNoHp() {
        return noHp;
    }
}
